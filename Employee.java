    public void RegNoGenerator(){
        
        Date curDate = new Date();
        SimpleDateFormat format = new SimpleDateFormat("YYYY");
        String datetostring = format.format(curDate);
        int year = Integer.parseInt(datetostring) % 100;
        
        connection connect = new connection();
        conn = (Connection)connect.Connectdb();
        
        String sql = "SELECT LastRegNo FROM registrationmonitoring";
        
        try {
            
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            if(rs.next()){
                
                String add = rs.getString("LastRegNo");
                int add1 = Integer.parseInt(add);
                
                regno = add1 + 1;
                
                if(regno < 10){
                    var ="000";
                }
                if(regno > 10 && regno<100){
                    var="00";
                }
                if(regno >100 && regno<1000){
                    var="0";
                }
                employeeid.setText(code+year+var+regno);
                
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
    
