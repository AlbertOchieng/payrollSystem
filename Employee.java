    private void firstnameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_firstnameKeyTyped
       char c = evt.getKeyChar();
       
       if(!((c>='a' && c<='z') ||(c>='A' && c<='Z') || (c == KeyEvent.VK_BACKSPACE) || (c==KeyEvent.VK_DELETE) || (c==KeyEvent.VK_TAB))){
           getToolkit().beep();
           evt.consume();
           JOptionPane.showMessageDialog(this, "Enter alphabets only","warning",JOptionPane.WARNING_MESSAGE);
           
           int length = firstname.getText().length();
           
           if(length >=20){
               getToolkit().beep();
               evt.consume();
           }
       }
    }//GEN-LAST:event_firstnameKeyTyped

